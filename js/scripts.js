function load_enbudo()
{
  $('#DEnvudo').show();
  $("#DLista").hide();
  $("#DCronograma").hide();

}

function load_lista()
{
  $('#DEnvudo').hide();
  $("#DLista").show();
  $("#DCronograma").hide();

}

function load_cronograma()
{
  $('#DEnvudo').hide();
  $("#DLista").hide();
  $("#DCronograma").show();
}
